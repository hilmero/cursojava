public class SalidaJava {
    public static void main(String[] args)     {
       System.out.printf("%.2f %n", 12.3698);
       double n = 1.25036;
       System.out.printf("%.3f %n", n);
       System.out.printf("%+.3f %n", n);
       int x = 10;
       System.out.printf("%d %n", x);
       System.out.printf("%+d %n", x);
       System.out.printf("valor de n es %.2f y de x es %d \n", n, x);
       System.out.printf("n es %+10.2f \n", n);
       System.out.printf("n es %+010.2f \n", n);
       System.out.printf("%10s \n","Hola");
       System.out.printf("%-10s \n","Hola");
   }
}
